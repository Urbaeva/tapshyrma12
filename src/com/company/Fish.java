package com.company;

public class Fish {
    private String name;
    private String color;
    private double weight;

    void setName(String name) {
        this.name = name;
    }

    void setColor(String color) {
        this.color = color;
    }

    void setWeight(double weight) {
        this.weight = weight;
    }

    String getName() {
        return name;
    }

    String getColor() {
        return color;
    }

    double getWeight() {
        return weight;
    }
}
